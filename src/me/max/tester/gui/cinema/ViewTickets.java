    protected String getValue(String context, String reference) {
        String[] split = context.split("!-!-!");
        
        switch (reference) {
            case "user":
                return split[0];
            case "movie":
            case "film":
                return split[1];
            case "time":
                return split[2];
            case "seat":
                return split[3];
            case "quantity":
            case "amount":
                return split[4];
            case "parking":
                return split[5];
            case "price":
                return split[6];
              
        }
        return "null";
        
    }
    
    protected void searchTickets() {
        list.removeAllItems();
        for (Object line : fr.getFileContent("C_BOOKINGS")) {
            if (getValue(line.toString(), "user").equals(Methods.user)) list.addItem(getValue(line.toString(), "time") + ": Â£" + getValue(line.toString(), "price") + ".00");
        }
    }
    
    protected void formatButtons() {
        int index = 0;
        for(JToggleButton button : buttons) {
            index++;
            button.setText(fr.getLineFromFile("C_SCREENINGS", index));
        }
    }
    
    protected void updateMovieSelect() {
        for (JToggleButton button : buttons) {
            button.setIcon(null);
            if (button.isSelected()) {
                movieName = button.getText();
                //button.setIcon(new javax.swing.ImageIcon(getClass().getResource("/me/max/tester/gui/cinema/resources/arrow.png")));
            }
        }
    
    }
    
